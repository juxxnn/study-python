# Day 1 - Beginner
# Working with variables in Python to Manage Data.

# print() - função para imprimir na tela

print("Hello World!")

# Print Modifiers
# Para detonar uma fala usamos aspas, se quisermos que apareça no código, temos essas opções abaixo:

print("Ela disse \"Oi\" e foi embora")

print('Ela disse "Oi" e foi embora')

print("Ela disse 'Oi' e foi embora")

# Manipulação de string

# Utilizamos o \n para quebrar linha
print("Oi!\nTudo bem?")

# Utilizamos o + para concatenar strings
print("Meu nome é " + "Juliana")

# input() - função para receber dados do usuário
name = input("What is your name? ")
print(name)

# Ao invés de utilizar o print e o input em duas linhas, podemos fazer de uma só vez
print('Hello ' + input('Qual o seu nome?') + "!")

# len() - função de comprimento, retorna o número de itens de um objeto ou se for uma string, retorna o número de caracteres

name = input("What is your name?")
length = len(name)
print(length)

# Variáveis

# É um objeto (uma posição, frequentemente localiazada em memória) capaz de reter e representar uma expressão. Pode ser alterado, é variável.
# Não podemos começar com um número na variável, espaço em branco, utilizar caracteres especiais e palavras reservadas.

# Variáveis globais: podem ser acessadas em qualquer parte do código.
# Variáveis locais: podem ser acessadas apenas no bloco de código onde foram criadas.

name = "Juliana"